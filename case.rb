weather = 'sunny'

case weather 
when'rainy'
	puts "Take an umbrella!"
when 'cloudy'
	puts "Wear a sweater!"
when 'snowy'
	puts "Wear snow pants!"
when 'freezing'
	puts "Wear 2 pants"
when 'hot'
	puts "Wear shorts and flip flops!"
when 'windy'
	puts "Tie up long hair!"
else 
	puts "Hmm, well I don't know what to wear in that kind of weather!"
end 

